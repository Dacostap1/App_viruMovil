package com.example.viruapp.Model;

public class Modul {
    private int id;
    private String name;
    private String solicitud;
    private String memorandum;
    private String informe;
    private String recibo;
    private String proyecto;
    private String f_supervision;
    private String f_evaluacion;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSolicitud() {
        return solicitud;
    }

    public void setSolicitud(String solicitud) {
        this.solicitud = solicitud;
    }

    public String getMemorandum() {
        return memorandum;
    }

    public void setMemorandum(String memorandum) {

        this.memorandum = memorandum;
    }

    public String getInforme() {
        return informe;
    }

    public void setInforme(String informe) {
        this.informe = informe;
    }

    public String getRecibo() {
        return recibo;
    }

    public void setRecibo(String recibo) {
        this.recibo = recibo;
    }

    public String getProyecto() {
        return proyecto;
    }

    public void setProyecto(String proyecto) {
        this.proyecto = proyecto;
    }

    public String getF_supervision() {
        return f_supervision;
    }

    public void setF_supervision(String f_supervision) {
        this.f_supervision = f_supervision;
    }

    public String getF_evaluacion() {
        return f_evaluacion;
    }

    public void setF_evaluacion(String f_evaluacion) {
        this.f_evaluacion = f_evaluacion;
    }
}
